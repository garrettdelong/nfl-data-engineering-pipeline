    AND (
      COALESCE(stg_pbp.punt_attempt, 0) = 1
      OR COALESCE(stg_pbp.kickoff_attempt, 0) = 1
      OR COALESCE(stg_pbp.field_goal_attempt, 0) = 1
      OR COALESCE(stg_pbp.extra_point_attempt, 0) = 1
    )
