    stg_pbp.defteam,
    stg_pbp.side_of_field,
    stg_pbp.game_date,
    stg_pbp.quarter_seconds_remaining,
    stg_pbp.half_seconds_remaining,
    stg_pbp.game_seconds_remaining,
    stg_pbp.game_half,
    stg_pbp.goal_to_go,
    stg_pbp.yrdln,
    stg_pbp.first_down_penalty,
    stg_pbp.replay_or_challenge,
    stg_pbp.replay_or_challenge_result,
    stg_pbp.two_point_attempt,
    stg_pbp.two_point_conv_result,
    stg_pbp.aborted_play,
    stg_pbp.wpa,
    stg_pbp.vegas_wpa,
    stg_pbp.play_type_nfl,
    stg_pbp.st_play_type,
    stg_pbp.special_teams_play,
    stg_pbp.play_flag,
    stg_pbp.special_flag,
    stg_pbp.pass_attempt,
    stg_pbp.rush_attempt,
    stg_pbp.sack,
    stg_pbp.qb_scramble,
    stg_pbp.punt_attempt,
    stg_pbp.kickoff_attempt,
    stg_pbp.field_goal_attempt,
    stg_pbp.extra_point_attempt
  FROM {{ ref('fct_play') }} AS fct_play
  INNER JOIN {{ ref('stg_pbp') }} AS stg_pbp
    ON stg_pbp.game_id = fct_play.game_id
    AND stg_pbp.play_id = fct_play.play_id
  WHERE fct_play.drive IS NOT NULL
    AND COALESCE(stg_pbp.two_point_attempt, 0) = 0
    AND (
      COALESCE(fct_play.penalty, 0) = 1
      OR fct_play.penalty_team IS NOT NULL
      OR fct_play.penalty_player_id IS NOT NULL
      OR fct_play.penalty_yards IS NOT NULL
      OR fct_play.penalty_type IS NOT NULL
    )
)

