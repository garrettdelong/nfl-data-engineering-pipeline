    AND COALESCE(stg_pbp.qb_spike, 0) = 0
    AND COALESCE(stg_pbp.qb_kneel, 0) = 0
    AND (
      COALESCE(stg_pbp.pass_attempt, 0) = 1
      OR COALESCE(stg_pbp.sack, 0) = 1
      OR COALESCE(stg_pbp.qb_scramble, 0) = 1
    )
