  SELECT
    fct_play.game_id,
    fct_play.play_id,
    fct_play.season,
    fct_play.nfl_week,
    fct_play.qtr,
    fct_play.drive,
    fct_play.down,
    fct_play.ydstogo,
    fct_play.yardline_100,
    fct_play.play_type,
    fct_play.play_desc,
    fct_play.offense_franchise_id,
    fct_play.defense_franchise_id,
    fct_play.rusher_player_id,
    fct_play.yards_gained,
    fct_play.rushing_yards,
    fct_play.epa,
    fct_play.wp,
    fct_play.success,
    fct_play.touchdown,
    fct_play.rush_touchdown,
    fct_play.fumble,
    fct_play.fumble_lost,
    fct_play.fumble_forced,
    fct_play.series,
    fct_play.penalty_team,
    fct_play.penalty_player_id,
    fct_play.penalty_player_name,
    fct_play.penalty_yards,
    fct_play.penalty_type,
    fct_play.penalty,
    stg_pbp.posteam,
