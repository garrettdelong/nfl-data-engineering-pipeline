    stg_pbp.sack,
    stg_pbp.qb_scramble,
    stg_pbp.qb_kneel,
    stg_pbp.shotgun,
    stg_pbp.no_huddle,
    stg_pbp.run_location,
    stg_pbp.run_gap,
    stg_pbp.first_down_rush,
    stg_pbp.first_down_penalty,
    stg_pbp.third_down_converted,
    stg_pbp.third_down_failed,
    stg_pbp.fourth_down_converted,
    stg_pbp.fourth_down_failed,
    stg_pbp.tackled_for_loss,
    stg_pbp.rush_touchdown AS stg_rush_touchdown,
    stg_pbp.rush_flag,
    stg_pbp.play_flag,
    stg_pbp.special_flag,
    stg_pbp.two_point_attempt,
    stg_pbp.two_point_conv_result,
    stg_pbp.aborted_play,
    stg_pbp.wpa,
    stg_pbp.vegas_wpa,
    stg_pbp.qb_epa,
    stg_pbp.rusher,
    stg_pbp.rusher_jersey_number,
    stg_pbp.lateral_rush,
    stg_pbp.lateral_rusher_player_id,
    stg_pbp.lateral_rusher_player_name,
    stg_pbp.lateral_rushing_yards
  FROM {{ ref('fct_play') }} AS fct_play
  INNER JOIN {{ ref('stg_play_by_play') }} AS stg_pbp
    ON stg_pbp.game_id = fct_play.game_id
    AND stg_pbp.play_id = fct_play.play_id
  WHERE fct_play.drive IS NOT NULL
    AND fct_play.play_type IS NOT NULL
    AND LOWER(fct_play.play_type) != 'no_play'
    AND COALESCE(stg_pbp.two_point_attempt, 0) = 0
    AND COALESCE(stg_pbp.qb_kneel, 0) = 0
    AND COALESCE(stg_pbp.sack, 0) = 0
    AND (
      COALESCE(stg_pbp.rush_attempt, 0) = 1
      OR COALESCE(stg_pbp.qb_scramble, 0) = 1
    )
)

SELECT
  rush_plays.game_id,
  rush_plays.play_id,
  rush_plays.season,
  rush_plays.nfl_week,
  rush_plays.qtr,
